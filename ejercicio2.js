// Ejercicio 2: Determinar el dia de la semana 
let dia = 3; // Cambia la variables 'dia' para ver los diferentes dias cambiando el numero

switch (dia) {
    case 1:
        console.log("Lunes");
        break;
        case 2:
            console.log("Martes");
            break;
            case 3:
                console.log("Miercoles");
                break;
                case 4:
                    console.log("Jueves");
                    break;
                    case 5:
                        console.log("Viernes");
                        break;
                        case 6:
                            console.log("Sabado");
                            break;
                            case 7:
                                console.log("Domingo");
                                break;
                                default:
                                    console.log("Numero de dia invalido.");
}

// cambia la variable 'dia' para ver los diferentes dias